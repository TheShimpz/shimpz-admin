BY_KEY = {f["key"]: f for f in SCHEMA}

# Per-credential acquisition guide — the wizard renders this as an expandable, numbered
# "How to get this →" with a direct link to the provider. Only the credentials a human must
# actually hunt for get an entry; generated/advanced keys rely on their one-line `help`.
GUIDES = {}


def field(key):
    """Exact lookup; unknown key raises (the fail-fast contract every caller relies on)."""
    try:
        return BY_KEY[key]
    except KeyError:
        raise ValueError(f"unknown key {key!r} — not in the agent keyset") from None


def generate_internal(existing):
    """Mint values for `generate` keys that are absent/empty in `existing` → {key: new_value}.

    Never overwrites a present value (rotation is a deliberate act, not a side effect).
    """
    return {
        f["key"]: secrets.token_urlsafe(24) for f in SCHEMA if f["generate"] and not existing.get(f["key"], "").strip()
    }
